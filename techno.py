color = input("กรุณาใส่สีของไฟจราจร (แดง, เหลือง, เขียว): ")

# ตรวจสอบและแสดงผลตามเงื่อนไข
if color == "แดง":
    print("ไฟแดง หยุดรถ")
elif color == "เหลือง":
    print("ไฟเหลือง เตรียมหยุด")
elif color == "เขียว":
    print("ไฟเขียว ออกรถ")
else:
    print("กรุณาใส่สีให้ถูกต้อง (แดง, เหลือง, เขียว)")